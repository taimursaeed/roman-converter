import RomanNumerals from './RomanNumerals';
test('Integer to Roman', () => {
  expect(RomanNumerals.toRoman('88')).toBe('LXXXVIII');
  expect(RomanNumerals.toRoman('5')).toBe('V');
  expect(RomanNumerals.toRoman('9')).toBe('IX');
  expect(RomanNumerals.toRoman('12')).toBe('XII');
  expect(RomanNumerals.toRoman('16')).toBe('XVI');
  expect(RomanNumerals.toRoman('29')).toBe('XXIX');
  expect(RomanNumerals.toRoman('44')).toBe('XLIV');
  expect(RomanNumerals.toRoman('45')).toBe('XLV');
  expect(RomanNumerals.toRoman('68')).toBe('LXVIII');
  expect(RomanNumerals.toRoman('83')).toBe('LXXXIII');
  expect(RomanNumerals.toRoman('97')).toBe('XCVII');
  expect(RomanNumerals.toRoman('99')).toBe('XCIX');
  expect(RomanNumerals.toRoman('500')).toBe('D');
  expect(RomanNumerals.toRoman('501')).toBe('DI');
  expect(RomanNumerals.toRoman('649')).toBe('DCXLIX');
  expect(RomanNumerals.toRoman('798')).toBe('DCCXCVIII');
  expect(RomanNumerals.toRoman('891')).toBe('DCCCXCI');
  expect(RomanNumerals.toRoman('1000')).toBe('M');
  expect(RomanNumerals.toRoman('1004')).toBe('MIV');
  expect(RomanNumerals.toRoman('1006')).toBe('MVI');
  expect(RomanNumerals.toRoman('1023')).toBe('MXXIII');
  expect(RomanNumerals.toRoman('2014')).toBe('MMXIV');
  expect(RomanNumerals.toRoman('3999')).toBe('MMMCMXCIX');
});
